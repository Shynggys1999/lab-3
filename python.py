if a < b: #Егер а b-дан кіші болса
    for i in range(a, b + 1):#a-дан b-ға дейінгі сандарды өсу реті бойынша шығаруға range функц қолданамыз
        print(i)
else:  # Егер шарт орындалмаса
    for i in range(a, b - 1, -1): #a-дан b-ға дейінгі сандарды кему реті бойынша шығаруға range функц қолданамыз
        print(i)


